#include "libft.h"

void	ft_print_arr(char **arr)
{
	size_t	i;

	i = 0;
	while (arr[i])
	{
		write(1, arr[i], ft_strlen(arr[i]));
		write(1, "\n", 1);
		i++;
	}
}
